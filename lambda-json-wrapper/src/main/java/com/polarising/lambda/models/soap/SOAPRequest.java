package com.polarising.lambda.models.soap;

public class SOAPRequest {
	
	private String body;
	
	public SOAPRequest() {}

	public SOAPRequest(String body) {
		super();
		this.body = body;
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}
	
	

}
